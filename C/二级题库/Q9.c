//1~1000能被5或13整除但不能同时被5和13整除的数存放在arr[]中返回
#include<stdio.h>
#include<stdlib.h>
void proc(int * arr,int * n){
    int i,j = 0;
    for(i=1;i<=1000;i++){
        if((i%5==0 || i%13==0) && i%65!=0)arr[j++] = i;
        * n = j;
    }
}
int main(){
    int arr[1000],n,k;
    proc(arr,&n);
    for(k=0;k<n;k++)printf("%d ",arr[k]);
    printf("\n");
    return 0;
}
